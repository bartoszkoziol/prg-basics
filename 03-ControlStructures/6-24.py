for i in range(1,6):
    print(i * "*")

for i in range(4,0,-1):
    print(i * "*")


###
#W Pythonie funkcja range() działa w określony sposób:

#range(start, stop, step): Trzy argumenty oznaczają:
#start — początkowa wartość sekwencji.
#stop — wartość, przy której pętla się kończy, ale ta wartość nie jest już zawarta w zakresie.
#step — krok, czyli o ile wartość ma się zmieniać z każdym krokiem pętli.